package java_test;

public class person {
	int age;
	String name;
	char marriage;
	int children_num;
	
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public char getMarriage() {
		return marriage;
	}
	public void setMarriage(char marriage) {
		this.marriage = marriage;
	}
	public int getChildren_num() {
		return children_num;
	}
	public void setChildren_num(int children_num) {
		this.children_num = children_num;
	}
}
